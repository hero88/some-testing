export const getProvinceOptions = () => {
  const keys = Object.keys(PROVINCE)

  return keys.map(key => ({
    value: PROVINCE[key].VALUE,
    label: PROVINCE[key].LABEL
  }))
}

export const getProvinceLabel = (provinceValue) => {
  const provinces = Object.keys(PROVINCE)
  const name = provinces.find((item) => {
    return PROVINCE[item].VALUE === provinceValue
  })

  return name ? PROVINCE[name].LABEL : ''
}

export const getProvinces = (intl) => {
  const keys = Object.keys(PROVINCE)

  return keys.map(key => ({
    id: PROVINCE[key].VALUE,
    name: intl.formatMessage({ id: PROVINCE[key].LABEL })
  }))
}
